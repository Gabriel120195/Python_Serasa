
def divisao(num1, num2):
    if(num2 != 0):
        divi = num1 / num2
        return divi
    else:
        return False

def velocidade(distancia, tempo):
    if divisao((distancia, tempo) != False):
        velocidade = divisao(distancia, tempo)
        return velocidade
    else:
        return "Não é possivel dividir por zero!"